async def run_art50_cycle() -> dict:
    """
    Scheduler-Einstieg: alle registrierten Shops scannen.
    Schickt TG-Alert bei nicht-konformen Shops.
    """
    state    = _load_state()
    shops    = state.get("registered_shops", [])
    results  = {}
    alerts   = []

    for shop in shops:
        try:
            scan = await scan_website_for_ai_content(shop)
            results[shop] = scan
            if scan.get("found_ai_widgets") and not scan.get("has_disclosure"):
                alerts.append(f"⚠️ {shop} — Chatbot ohne Disclosure!")
        except Exception as e:
            results[shop] = {"ok": False, "error": str(e)}

    days = max(0, (DEADLINE_TS - int(time.time())) // 86400)

    if alerts:
        msg = (
            f"🚨 *EU AI Act Art. 50 — {len(alerts)} Verstöße gefunden!*\n"
            f"Deadline: {DEADLINE_DATE} (noch {days} Tage)\n"
            f"Bußgeld: bis €15 Mio. / 3% Umsatz\n\n"
            + "\n".join(alerts[:10])
        )
        await _tg(msg)
    elif days <= 30:
        await _tg(
            f"⏰ *EU AI Act Art. 50 — Deadline in {days} Tagen!*\n"
            f"({DEADLINE_DATE}) — alle {len(shops)} Shops geprüft, keine Akutprobleme."
        )

    state["scan_results"] = results
    state["last_cycle"]   = int(time.time())
    _save_state(state)

    return {
        "ok":      True,
        "shops":   len(shops),
        "alerts":  len(alerts),
        "days":    days,
        "results": results,
    }


