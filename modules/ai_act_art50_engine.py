async def scan_website_for_ai_content(url: str) -> dict:
    """
    Scannt eine Website auf AI-Widgets und fehlende Art.-50-Disclosures.
    Returns: {found_ai_widgets, compliance_score, missing_disclosures, checklist}
    """
    if not url.startswith("http"):
        url = "https://" + url

    found_widgets   = []
    has_disclosure  = False
    html_content    = ""

    try:
        async with aiohttp.ClientSession() as session:
            async with session.get(
                url,
                headers={"User-Agent": "Mozilla/5.0 (EU-AI-Act-Compliance-Scanner/1.0)"},
                timeout=aiohttp.ClientTimeout(total=15),
                ssl=False,
            ) as r:
                if r.status == 200:
                    html_content = (await r.text())[:50000]
    except Exception as e:
        return {
            "ok": False, "url": url, "error": str(e),
            "compliance_score": 0, "found_ai_widgets": [],
            "missing_disclosures": ["Website nicht erreichbar"],
        }

    html_lower = html_content.lower()

    # AI-Widgets suchen
    for signal in AI_WIDGET_SIGNALS:
        if signal in html_lower:
            found_widgets.append(signal)

    # Disclosure-Hinweise suchen
    disclosure_signals = [
        "ai act", "eu ai", "künstliche intelligenz", "artificial intelligence",
        "ki-assistent", "ai-assistant", "chatbot-hinweis", "bot-disclosure",
        "data-ai-act", "data-ai-generated",
    ]
    for sig in disclosure_signals:
        if sig in html_lower:
            has_disclosure = True
            break

    # Compliance-Checkliste
    checklist = []
    for item in ART50_CHECKLIST:
        passed = False
        if item["check"] == "chatbot_disclosure":
            passed = has_disclosure or not found_widgets
        elif item["check"] == "machine_readable_mark":
            passed = 'data-ai-act-compliant' in html_lower
        else:
            passed = False  # konservativ — manuelle Prüfung nötig

        checklist.append({**item, "passed": passed, "manual_check": not passed})

    passed_count = sum(1 for c in checklist if c["passed"])
    score        = round(passed_count / len(checklist) * 100)

    missing = []
    if found_widgets and not has_disclosure:
        missing.append(f"Chatbot-Disclosure fehlt (gefunden: {', '.join(found_widgets[:3])})")
    if 'data-ai-act-compliant' not in html_lower:
        missing.append("Maschinenlesbare KI-Markierung (data-ai-act-compliant) fehlt")
    if not missing and not found_widgets:
        missing.append("Kein KI-Chatbot gefunden — kein Art.-50-Risiko bei aktuellem Stand")

    return {
        "ok":                   True,
        "url":                  url,
        "found_ai_widgets":     list(set(found_widgets)),
        "has_disclosure":       has_disclosure,
        "compliance_score":     score,
        "missing_disclosures":  missing,
        "checklist":            checklist,
        "scan_timestamp":       datetime.now(timezone.utc).isoformat(),
        "risk_level":           "HOCH" if (found_widgets and not has_disclosure) else "NIEDRIG",
        "days_to_deadline":     max(0, (DEADLINE_TS - int(time.time())) // 86400),
    }


